    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String phone = jTextField4.getText();
        String email = jTextField3.getText();
        String address = jTextArea2.getText();
        
        Owner owner = new Owner();
        try{
            int id = Integer.parseInt(jTextField2.getText());
            if(name.trim().equals("") || phone.trim().equals("") || email.trim().equals("") || address.trim().equals("")){
                JOptionPane.showMessageDialog(null, "Please Enter the required Information", "Edit owner",1);
            }
            else{
                if(owner.editOwner(new Owner(id,name,phone,email,address))){
                    JOptionPane.showMessageDialog(null, "Owner edited", "Edit owner",1);
                }
            else{
                JOptionPane.showMessageDialog(null, "Owner NOT edited", "Edit owner",2);
            }
        }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage()+"Enter a valid Owner ID", "Invalid ID",0);
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try{
            int id = Integer.parseInt(jTextField2.getText());
            Owner owner = new Owner();
            if(jTextField2.getText().trim().equals("")){
                JOptionPane.showMessageDialog(null, "Enter the Owner ID", "Empty owner ID",2);
            }
            else{
                int confirm = JOptionPane.showConfirmDialog(null, "Do you want to Delete this Owner?","Delete Owner",JOptionPane.YES_NO_OPTION);
                if(confirm == JOptionPane.YES_OPTION){
                    if(owner.deleteOwner(id)){
                        JOptionPane.showMessageDialog(null, "Owner Deleted", "Delete Owner",2);
                        jTextField1.setText("");
                        jTextField2.setText("");
                        jTextField3.setText("");
                        jTextField4.setText("");
                        jTextArea2.setText("");
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Operation Failed", "Delete Owner",2);
                    }
                }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage()+"Enter a valid Owner ID", "Invalid ID",0);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

