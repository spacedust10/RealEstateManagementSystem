    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String phone = jTextField4.getText();
        String email = jTextField3.getText();
        String address = jTextArea2.getText();
        
        Owner owner = new Owner();
        if(name.trim().equals("") || phone.trim().equals("") || email.trim().equals("") || address.trim().equals("")){
            JOptionPane.showMessageDialog(null, "Please Enter the required Information", "add owner",1);
        }
        else{
            if(owner.addNEWowner(new Owner(0,name,phone,email,address))){
                JOptionPane.showMessageDialog(null, "NEW Owner added", "add owner",1);
            }
            else{
                JOptionPane.showMessageDialog(null, "Owner NOT added", "add owner",2);
            }
        } 
    }//GEN-LAST:event_jButton1ActionPerformed

