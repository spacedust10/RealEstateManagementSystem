    public void fillwithOwnersdata(){
        Owner owner = new Owner();
        ArrayList<Owner> ownerList = owner.ownersList();
        
        String[] colNames = {"ID","Name","Phone","Email","Address"};
        
        Object[][] rows = new Object[ownerList.size()][6];
        
        for(int i = 0 ; i<ownerList.size() ; i++){
            rows[i][0] = ownerList.get(i).getId();
            rows[i][1] = ownerList.get(i).getname();
            rows[i][2] = ownerList.get(i).getphone();
            rows[i][3] = ownerList.get(i).getemail();
            rows[i][4] = ownerList.get(i).getaddress();
        }
        DefaultTableModel model = new DefaultTableModel(rows,colNames);
        jTable1.setModel(model);
        
         jTable1.setRowHeight(30);
    }
